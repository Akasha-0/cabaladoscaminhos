// GET /api/hatha/data - Get all Hatha yoga data
export async function GET(request: NextRequest) {
  try {
    const searchParams = request.nextUrl.searchParams;
    const type = searchParams.get('type');

    if (type === 'asanas') {
      return NextResponse.json({
        success: true,
        data: HATHA_ASANAS,
        count: HATHA_ASANAS.length,
      });
    }

    if (type === 'pranayama') {
      return NextResponse.json({
        success: true,
        data: HATHA_PRANAYAMA,
        count: HATHA_PRANAYAMA.length,
      });
    }

    if (type === 'shatkriyas') {
      return NextResponse.json({
        success: true,
        data: HATHA_SHATKRIYA,
        count: HATHA_SHATKRIYA.length,
      });
    }

    if (type === 'mudras') {
      return NextResponse.json({
        success: true,
        data: HATHA_MUDRAS,
        count: HATHA_MUDRAS.length,
      });
    }

    if (type === 'bandhas') {
      return NextResponse.json({
        success: true,
        data: HATHA_BANDHAS,
        count: HATHA_BANDHAS.length,
      });
    }

    if (type === 'surya-namaskar') {
      return NextResponse.json({
        success: true,
        data: HATHA_SURYA_NAMASKAR,
      });
    }

    // Return all Hatha data
    return NextResponse.json({
      success: true,
      data: {
        asanas: HATHA_ASANAS,
        pranayama: HATHA_PRANAYAMA,
        shatkriyas: HATHA_SHATKRIYA,
        mudras: HATHA_MUDRAS,
        bandhas: HATHA_BANDHAS,
        suryaNamaskar: HATHA_SURYA_NAMASKAR,
      },
      counts: {
        asanas: HATHA_ASANAS.length,
        pranayama: HATHA_PRANAYAMA.length,
        shatkriyas: HATHA_SHATKRIYA.length,
        mudras: HATHA_MUDRAS.length,
        bandhas: HATHA_BANDHAS.length,
      },
    });
  } catch (error) {
    console.error('Error in Hatha data API:', error);
    return NextResponse.json(
      { success: false, error: 'Failed to retrieve Hatha data' },
      { status: 500 }
    );
  }
}