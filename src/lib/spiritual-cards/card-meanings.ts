export const allCards: CardMeaning[] = [
  ...tarotArcanosMaiores,
  ...tarotArcanosMenores,
  ...lenormandCards,
  ...oraculoCabala,
];

// ============================================================
// UTILITY FUNCTIONS
// ============================================================

export function getMeanings(): {
  tarot: {
    maiores: CardMeaning[];
    menores: CardMeaning[];
  };
  lenormand: CardMeaning[];
  cabala: CardMeaning[];
  all: CardMeaning[];
} {
  return {
    tarot: {
      maiores: tarotArcanosMaiores,
      menores: tarotArcanosMenores,
    },
    lenormand: lenormandCards,
    cabala: oraculoCabala,
    all: allCards,
  };
}

export function getCardById(id: string): CardMeaning | undefined {
  return allCards.find(card => card.id === id);
}

export function getCardsByArcano(arcano: 'maior' | 'menor'): CardMeaning[] {
  return allCards.filter(card => card.arcano === arcano);
}

export function getCardsBySuit(suit: string): CardMeaning[] {
  return allCards.filter(card => card.suit === suit);
}

export function getCardsByOrixa(orixa: string): CardMeaning[] {
  return allCards.filter(card => card.orixa?.toLowerCase() === orixa.toLowerCase());
}

export function getCardsByElement(element: string): CardMeaning[] {
  return allCards.filter(card => card.element?.toLowerCase() === element.toLowerCase());
}

export function getCardsByChakra(chakra: string): CardMeaning[] {
  return allCards.filter(card => card.chakra?.toLowerCase() === chakra.toLowerCase());
}

export function searchCards(query: string): CardMeaning[] {
  const lowerQuery = query.toLowerCase();
  return allCards.filter(card => 
    card.name.toLowerCase().includes(lowerQuery) ||
    card.significado.toLowerCase().includes(lowerQuery) ||
    card.keywords.some(k => k.toLowerCase().includes(lowerQuery)) ||
    card.orixa?.toLowerCase().includes(lowerQuery)
  );
}